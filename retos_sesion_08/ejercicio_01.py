# Ingresa por teclado dos coordenadas 'x','y' y una ubicación, almacena los 3 valores en una tupla e imprime el resultado

coord_1 = float(input("Escriba primera coordenada: "))
coord_2 = float(input("Escriba segunda coordenada: "))
ubicacion = input("Escriba la ubicación: ")

tupla = (coord_1, coord_2, ubicacion)
print("Contenido de la tupla es:", tupla)
